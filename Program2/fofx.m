function y = fofx(x)
% function y = fofx(x)
%
% fofx evaluates the value of the function y = cos(x) - sin(x) at x
%
% On Input:
% x - a real number 
%
% On Ouput:
% y - the value of the function y = cos(x) - sin(x) at x
%
% No external function calls in fofx.
% Author: Aneesh Komanduri


    y = cos(x) - sin(x);


end